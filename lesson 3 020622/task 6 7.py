# —Ä–µ–≥–∏—Å—Ç—Ä–µ. –ù—É–∂–Ω–æ —Å–¥–µ–ª–∞—Ç—å –≤—ã–≤–æ–¥ –∏—Å—Ö–æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏, –Ω–æ –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ –¥–æ–ª–∂–Ω–æ
# –Ω–∞—á–∏–Ω–∞—Ç—å—Å—è —Å –∑–∞–≥–ª–∞–≤–Ω–æ–π –±—É–∫–≤—ã. –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –Ω–∞–ø–∏—Å–∞–Ω–Ω—É—é —Ä–∞–Ω–µ–µ —Ñ—É–Ω–∫—Ü–∏—é
# int_func().

def int_func(token):
    """–ü—Ä–∏–Ω–∏–º–∞–µ—Ç —Å–ª–æ–≤–∞ –∏–∑ –º–∞–ª–µ–Ω—å–∫–∏—Ö –ª–∞—Ç–∏–Ω—Å–∫–∏—Ö –±—É–∫–≤ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∏—Ö –∂–µ,
    –Ω–æ —Å –ø—Ä–æ–ø–∏—Å–Ω–æ–π –ø–µ—Ä–≤–æ–π –±—É–∫–≤–æ–π

    :param str token: –°–ª–æ–≤–æ.
    :return str: –°–ª–æ–≤–æ —Å –ø—Ä–æ–ø–∏—Å–Ω–æ–π –ø–µ—Ä–≤–æ–π –±—É–∫–≤–æ–π.
    """
    if type(token) is not str:
        return ""
    return token.capitalize()


def capitalize_every_word(text):
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Ç–µ–∫—Å—Ç, —Å–¥–µ–ª–∞–≤ –±–æ–ª—å—à–æ–π –∫–∞–∂–¥—É—é –ø–µ—Ä–≤—É—é –±—É–∫–≤—É –≤ –∫–∞–∂–¥–æ–º —Å–ª–æ–≤–µ.

    :param str text: —Ç–µ–∫—Å—Ç
    :return str: –∏–∑–º–µ–Ω–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç
    """
    if type(text) is not str:
        return ""
    return " ".join(map(int_func, text.split()))


def has_nonlatin_or_upper_symbs(text):
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —á—Ç–æ –≤ —Ç–µ–∫—Å—Ç–µ –µ—Å—Ç—å –Ω–µ —Ç–æ–ª—å–∫–æ –º–∞–ª–µ–Ω—å–∫–∏–µ –ª–∞—Ç–∏–Ω—Å–∫–∏–µ –±—É–∫–≤—ã

    :param str text: —Ç–µ–∫—Å—Ç
    :return bool:
    """
    letters = list("abcdefghijklmnopqrstuwyxz")
    for char in text:
        if char not in letters and char != " ":
            return True
    return False


stop_words = ["q", "–π", "—Å—Ç–æ–ø", "stop", "cnjg", "—Ñ–ª—é–≥–µ–≥–µ—Ö–∞–π–º–µ–Ω"]
