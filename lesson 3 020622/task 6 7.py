# —Ä–µ–≥–∏—Å—Ç—Ä–µ. –ù—É–∂–Ω–æ —Å–¥–µ–ª–∞—Ç—å –≤—ã–≤–æ–¥ –∏—Å—Ö–æ–¥–Ω–æ–π —Å—Ç—Ä–æ–∫–∏, –Ω–æ –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ –¥–æ–ª–∂–Ω–æ
# –Ω–∞—á–∏–Ω–∞—Ç—å—Å—è —Å –∑–∞–≥–ª–∞–≤–Ω–æ–π –±—É–∫–≤—ã. –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –Ω–∞–ø–∏—Å–∞–Ω–Ω—É—é —Ä–∞–Ω–µ–µ —Ñ—É–Ω–∫—Ü–∏—é
# int_func().

def int_func(token):
    """–ü—Ä–∏–Ω–∏–º–∞–µ—Ç —Å–ª–æ–≤–∞ –∏–∑ –º–∞–ª–µ–Ω—å–∫–∏—Ö –ª–∞—Ç–∏–Ω—Å–∫–∏—Ö –±—É–∫–≤ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∏—Ö –∂–µ,
    –Ω–æ —Å –ø—Ä–æ–ø–∏—Å–Ω–æ–π –ø–µ—Ä–≤–æ–π –±—É–∫–≤–æ–π

    :param str token: –°–ª–æ–≤–æ.
    :return str: –°–ª–æ–≤–æ —Å –ø—Ä–æ–ø–∏—Å–Ω–æ–π –ø–µ—Ä–≤–æ–π –±—É–∫–≤–æ–π.
    """
    latin_small_chars = 'qwertyuiopasdfghjklzxcvbnm'
    if type(token) is not str or set(token).difference(latin_small_chars):
        return ""
    return token.capitalize()


stop_words = ["q", "–π", "—Å—Ç–æ–ø", "stop", "cnjg", "—Ñ–ª—é–≥–µ–≥–µ—Ö–∞–π–º–µ–Ω"]
stop = False
inp = ""
result = ""

print("–°–¥–µ–ª–∞—é –≤—Å–µ —Å–ª–æ–≤–∞ –≤ –≤–∞—à–µ–π —Ñ—Ä–∞–∑–µ —Å –±–æ–ª—å—à–æ–π –±—É–∫–≤—ã.")
while not inp:
    inp = input("–í–≤–µ–¥–∏—Ç–µ —Å–ª–æ–≤–∞, —Å–æ—Å—Ç–æ—è—â–∏–µ –∏–∑ –º–∞–ª–µ–Ω—å–∫–∏—Ö –ª–∞—Ç–∏–Ω—Å–∫–∏—Ö –±—É–∫–≤, "
                "–∏–ª–∏ 'Q' –¥–ª—è –≤—ã—Ö–æ–¥–∞: ").strip()
    if not inp:
        continue
    if inp in stop_words:
        print("–ù—É –∏ –ª–∞–¥–Ω–æ :(")
        stop = True
        break
    words = inp.split()
    for w in words:
        cap_w = int_func(w)
        if not cap_w:
            print(
                f"{'‚ö†Ô∏è':>8} –°–ª–æ–≤–∞ –¥–æ–ª–∂–Ω—ã —Å–æ—Å—Ç–æ—è—Ç—å –∏—Å–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ –∏–∑ –º–∞–ª–µ–Ω—å–∫–∏—Ö "
                f"–ª–∞—Ç–∏–Ω—Å–∫–∏—Ö –±—É–∫–≤.")
            inp = ""
            result = ""
            break
        else:
            result += f" {cap_w}"

if not stop:
    print(f"üèÅ –†–µ–∑—É–ª—å—Ç–∞—Ç: {result}")
